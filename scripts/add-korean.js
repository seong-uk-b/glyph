// Add Korean translations
let updatedCount = 0;
for (const [expression, korean] of Object.entries(koreanTranslations)) {
  const escapedExp = expression.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const escapedKorean = korean.replace(/'/g, "\\'");

  // Match the word entry and add meaningKo
  const pattern = new RegExp(
    `(\\{ expression: '${escapedExp}', reading: '[^']+', meaning: '[^']+')( \\})`,
    'g'
  );

  const newContent = content.replace(pattern, `$1, meaningKo: '${escapedKorean}'$2`);
  if (newContent !== content) {
    content = newContent;
    updatedCount++;
  }
}

fs.writeFileSync(n5Path, content);
console.log(`Updated ${updatedCount} words with Korean translations`);
